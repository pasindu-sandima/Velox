def get_colored_list(big1,order):
    reds=[]
    greens=[]
    blues=[]
    for i in big1:
        if i[1]==1:
            reds.append(i)
        if i[1]==2:
            greens.append(i)
        if i[1]==3:
            blues.append(i)
    reds.append(reds[0][3]**2+reds[1][3]**2+reds[2][3]**2)
    greens.append(greens[0][3]**2+greens[1][3]**2+greens[2][3]**2)
    blues.append(blues[0][3]**2+blues[1][3]**2+blues[2][3]**2)

    ##order [r,g,b]
    ##for high 2
    ##for medium 1
    ##for low 0
    
    cell_list=[[reds[order[0]],reds[3]],[greens[order[1]],greens[3]],[blues[order[2]],blues[3]]]
    ## sorting for order
    swapped=1
    while swapped==1:
        swapped=0
        for i in range(len(cell_list)-1):
            if cell_list[i][1]>cell_list[i+1][1]:
                temp=cell_list[i+1]
                cell_list[i+1]=cell_list[i]
                cell_list[i]=temp
                swapped=1

    return cell_list


